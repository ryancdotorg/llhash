void HMAC_SHA2_512_Clone(HMAC_SHA2_512_CTX *dst, const HMAC_SHA2_512_CTX *src) {
  // relies on .data being first
  if (src->datalen) {
    memcpy(dst, src, sizeof(*dst));
  } else {
    memcpy(dst + sizeof(dst->data), src + sizeof(dst->data), sizeof(*dst) - sizeof(dst->data));
  }
}

void HMAC_SHA2_512_Init(HMAC_SHA2_512_CTX *ctx, const void *key, size_t len) {
  uint8_t *hash = ctx->hmac + SHA2_512_BLOCK_LENGTH;

  SHA2_512_Init((SHA2_512_CTX*)ctx);
  memset(ctx->hmac, 0, sizeof(ctx->hmac));

  if (len > SHA2_512_BLOCK_LENGTH) {
    SHA2_512(hash, key, len);
  } else {
    memcpy(hash, key, len);
  }

  for (int i = 0; i < SHA2_512_BLOCK_LENGTH; ++i) {
    ctx->hmac[i] = 0x5c ^ hash[i]; // opad
    hash[i] ^= 0x36;               // ipad
  }

  ctx->bitlen = SHA2_512_BLOCK_LENGTH*8;
  // pre-process the first block of HASH(ipad || data)
  SHA2_512_Transform(ctx->state, hash, 1);
  memcpy(ctx->hmac + SHA2_512_BLOCK_LENGTH*2, pad2048, SHA2_512_BLOCK_LENGTH);
}

void HMAC_SHA2_512_Update(HMAC_SHA2_512_CTX *ctx, const uint8_t data[], size_t len) {
  SHA2_512_Update((SHA2_512_CTX*)ctx, data, len);
}

void HMAC_SHA2_512_Final(uint8_t hash[], HMAC_SHA2_512_CTX *ctx) {
  SHA2_512_Final(ctx->hmac + SHA2_512_BLOCK_LENGTH, (SHA2_512_CTX*)ctx);
  SHA2_512_Raw(hash, ctx->hmac, 3);
}

void HMAC_SHA2_512(uint8_t hash[], const uint8_t key[], size_t key_sz, const uint8_t msg[], size_t msg_sz) {
  HMAC_SHA2_512_CTX ctx;

  HMAC_SHA2_512_Init(&ctx, key, key_sz);
  HMAC_SHA2_512_Update(&ctx, msg, msg_sz);
  HMAC_SHA2_512_Final(hash, &ctx);
}
